## Launch App

## To launch shiny app:

## CLICK "Run" in the top-right of this window. Or

## PRESS CTRL + ENTER to run the following code:

shiny::runApp("ipums-api-viewer")


## ALTERNATIVLY: You can run type the above code, specifying the sub-directory that contains the app directly from console. OR you could open the script located in "ipums-api-viewer/app.R" and click "Run App"